const CACHE_NAME = 'lotterlearner-v1';

// Convert relative paths to absolute URLs for GitHub Pages compatibility
const ASSETS_TO_CACHE = [
  './',
  './index.html',
  './create-vocab.html',
  './manifest.json',
  './icon.svg',
  './version.json',
  './images/cake.jpg',
  './audio/congratulations.mp4',
  './audio/gg.mp4',
  './audio/good.mp4',
  './audio/happy-birthday.mp4',
  './audio/nice.mp4',
  './audio/oh-dear.mp4',
  './audio/pay-attention.mp4',
  './audio/proud.mp4',
  './audio/well-done.mp4'
].map(path => new URL(path, self.location.href).href);

// Install event - cache all static assets
self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then((cache) => {
        console.log('Caching app assets');
        return cache.addAll(ASSETS_TO_CACHE);
      })
      .then(() => self.skipWaiting())
  );
});

// Activate event - clean up old caches
self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (cacheName !== CACHE_NAME) {
            console.log('Deleting old cache:', cacheName);
            return caches.delete(cacheName);
          }
        })
      );
    }).then(() => self.clients.claim())
  );
});

// Fetch event - serve from cache, fall back to network
self.addEventListener('fetch', (event) => {
  // Skip non-GET requests
  if (event.request.method !== 'GET') {
    return;
  }

  // Skip cross-origin requests (like Google Fonts)
  if (!event.request.url.startsWith(self.location.origin)) {
    return;
  }

  event.respondWith(
    caches.match(event.request)
      .then((cachedResponse) => {
        if (cachedResponse) {
          return cachedResponse;
        }

        return fetch(event.request).then((response) => {
          // Don't cache non-successful responses
          if (!response || response.status !== 200 || response.type !== 'basic') {
            return response;
          }

          // Clone the response since it can only be consumed once
          const responseToCache = response.clone();

          caches.open(CACHE_NAME)
            .then((cache) => {
              cache.put(event.request, responseToCache);
            });

          return response;
        });
      })
  );
});
